    private void buscar() {          

            
        try {
            EntradasSalidasLogicaNegocio logica = new EntradasSalidasLogicaNegocio();
            int max = Integer.parseInt(cboMax.getSelectedItem().toString());
            List<EntradasSalidas> result = logica.consultaAbierta(txtConsulta.getText(), max);
            
            //Llenar los datos en la jTable
            this.inicializarTabla();
            DefaultTableModel model = (DefaultTableModel) tblResultado.getModel();
            
            Object rowData[] = new Object[11];
            for (int i = 0; i < result.size(); i++) {
                rowData[0] = result.get(i).getId();
                rowData[1] = result.get(i).getPlaca().getPlaca();
                rowData[2] = result.get(i).getPlaca().getTipo();//tipo de vehiculo
                rowData[3] = "Horas"; //tarifa
                        
                rowData[4] = Utilidades.formatoFecha(result.get(i).getFechaHoraEntrada());
                if(result.get(i).getFechaHoraSalida() == null){
                    rowData[5] ="";
                }else{
                    rowData[5] = Utilidades.formatoFecha(result.get(i).getFechaHoraSalida());
                }
                    
                rowData[6] = result.get(i).getDescuento();
                rowData[7] = result.get(i).getValorPagar();
                rowData[8] = result.get(i).getObservacion();
                rowData[9] = result.get(i).getEntradaEmpleadosId().getNombres() + " " + result.get(i).getEntradaEmpleadosId().getApellidos();
                if (result.get(i).getSalidaEmpleadosId() == null){
                    rowData[10] = "";
                }else{
                    rowData[10] = result.get(i).getSalidaEmpleadosId().getNombres() + " " + result.get(i).getSalidaEmpleadosId().getApellidos();
                }
                model.addRow(rowData);
            }
            txtTotal.setText("" + result.size());
        } catch (Exception ex) {
            Utilidades.mensajeAdvertencia(ex.getMessage(), "Error en la consulta plsql");
            Logger.getLogger(GUIInformeAbierto.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
